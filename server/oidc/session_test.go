func TestSessionManager_CreateAndVerifyState(t *testing.T) {
	logger := newTestLogger(t)
	sm := oidc.NewSessionManager([]byte("test-secret-32-characters-long!!"), false, "", logger)

	w := httptest.NewRecorder()
	state, err := sm.CreateState(w)
	Ok(t, err)
	Assert(t, state != "", "expected non-empty state")

	r := httptest.NewRequest("GET", "/auth/callback?state="+state, nil)
	for _, c := range w.Result().Cookies() {
		r.AddCookie(c)
	}

	Ok(t, sm.VerifyState(r, state))
}

