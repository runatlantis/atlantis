func newTestLogger(t *testing.T) logging.SimpleLogging {
	t.Helper()
	return logging.NewNoopLogger(t)
}

func findCookie(cookies []*http.Cookie, name string) *http.Cookie {
	for _, c := range cookies {
		if c.Name == name {
			return c
		}
	}
	return nil
}

func TestSessionManager_SetAndGetSession(t *testing.T) {
	logger := newTestLogger(t)
	sm := oidc.NewSessionManager([]byte("test-secret-32-characters-long!!"), false, "", logger)

	email := "user@example.com"

	w := httptest.NewRecorder()
	Ok(t, sm.SetSession(w, email))

	sessionCookie := findCookie(w.Result().Cookies(), "atlantis_oidc")
	Assert(t, sessionCookie != nil, "expected atlantis_oidc cookie to be set")
	Assert(t, sessionCookie.HttpOnly, "expected cookie to be HttpOnly")
	Equals(t, http.SameSiteLaxMode, sessionCookie.SameSite)

	r := httptest.NewRequest("GET", "/", nil)
	r.AddCookie(sessionCookie)

	got, err := sm.GetSession(r)
	Ok(t, err)
	Equals(t, email, got)
}

